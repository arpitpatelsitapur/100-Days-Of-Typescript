// why typescript
// 1. static typing
// 2. modern javascript features
// 3. better tooling and editor support
// 4. large community and ecosystem
// 5. improved code quality and maintainability

// typescript execution vs javascript execution
// typescript is a superset of javascript
// typescript code is transpiled to javascript code
// the transpiled javascript code is then executed by the javascript engine

// javscript sample code
// const num=10;
// let message = "Hello, World!";
// let val=100;
// val='hello'; // works in javascript

// typescript sample code
// const num: number = 10;
// let message: string = "Hello, World!";
// let val: number = 100;
// val = 'hello'; // Error in TypeScript